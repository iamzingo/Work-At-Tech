import java.util.*;
import java.lang.*;
import java.io.*;


class WorkAtTech
{
	public static void main (String[] args) throws java.lang.Exception
	{
		Scanner sc = new Scanner(System.in);
        int num = sc.nextInt();
        
        for(int i=0 ; i<num ; i++){
            for(int k=0 ; k<=i ; k++){
                System.out.print("*");
            }
            System.out.println(" ");
        }
        sc.close();
	}
}
